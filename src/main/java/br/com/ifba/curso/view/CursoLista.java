    private void carregarTabela() {
  
        // Define o renderizador para as colunas com ícone
        tblCursos.getColumnModel().getColumn(4).setCellRenderer(new IconeRenderer("/imagens/delete.png"));
        tblCursos.getColumnModel().getColumn(5).setCellRenderer(new IconeRenderer("/imagens/update.png"));
    }
    
    class IconeRenderer extends javax.swing.table.DefaultTableCellRenderer {
    private final javax.swing.Icon icone;

    public IconeRenderer(String caminhoIcone) {
        // tenta carregar o ícone
        java.net.URL url = getClass().getResource(caminhoIcone);
        if (url != null) {
            icone = new javax.swing.ImageIcon(url);
        } else {
            System.err.println(" Ícone não encontrado: " + caminhoIcone);
            icone = null;
        }
    }

    @Override
    public java.awt.Component getTableCellRendererComponent(
            javax.swing.JTable table, Object value, boolean isSelected,
            boolean hasFocus, int row, int column) {

        javax.swing.JLabel label = new javax.swing.JLabel();
        label.setHorizontalAlignment(javax.swing.SwingConstants.CENTER);

        if (icone != null) {
            label.setIcon(icone);
        } else {
            label.setText("X"); // mostra algo se o ícone não carregar
        }

        if (isSelected) {
            label.setBackground(table.getSelectionBackground());
            label.setOpaque(true);
        }

        return label;
    }
}

