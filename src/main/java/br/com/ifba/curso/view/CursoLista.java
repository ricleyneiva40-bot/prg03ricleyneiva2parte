    private void tblCursosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCursosMouseClicked
        // TODO add your handling code here:
        int linha = tblCursos.rowAtPoint(evt.getPoint());
    int coluna = tblCursos.columnAtPoint(evt.getPoint());

    if (coluna == 4) { // coluna Excluir
        int confirm = JOptionPane.showConfirmDialog(this, 
                "Deseja excluir este item?", "Confirmação", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            ((DefaultTableModel) tblCursos.getModel()).removeRow(linha);
        }
    } else if (coluna == 5) { // coluna Editar
        CursoEditar telaEditar = new CursoEditar();
        telaEditar.setLocationRelativeTo(this);
        telaEditar.setVisible(true);
    }
    }//GEN-LAST:event_tblCursosMouseClicked

